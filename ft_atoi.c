#include "libft.h"

int	ft_atoi(const char *p)
{
	int		neg;
	long	res;

	res = 0;
	neg = 0;
	while ((*p >= 9 && *p <= 13) || *p == 32)
		p++;
	if (*p == '+' || *p == '-')
	{
		if (*p == '-')
			neg = 1;
		p++;
	}
	while (*p >= 48 && *p <= 57)
	{
		res = (res * 10) + (*p - '0');
		p++;
	}
	if (neg == 1)
		res *= -1;
	return ((int)res);
}
